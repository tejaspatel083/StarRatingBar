const container1 = document.querySelector('.rating1');
    const items1 = container1.querySelectorAll('.rating-item1')
    container1.onclick = e => {
      const elClass = e.target.classList;

      if (!elClass.contains('active')) {
        items1.forEach(
          item => item.classList.remove('active'));

        
        console.log(e.target.getAttribute("data-rate1"));
        elClass.add('active');
      }
    };
  
